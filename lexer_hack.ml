let (fixmes: Pos.t IMap.t IMap.t ref) = ref IMap.empty

let add_fixme err_nbr pos =
  let line, _, _ = Pos.info_pos pos in
  let line_value =
    match IMap.get line !fixmes with
    | None -> IMap.empty
    | Some x -> x
  in
  fixmes := IMap.add line (IMap.add err_nbr pos line_value) !fixmes;
  ()

